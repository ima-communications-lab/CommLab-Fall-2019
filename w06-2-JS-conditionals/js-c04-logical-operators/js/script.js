// Logical Operators

console.log( "or: ");

console.log( true || true );
console.log( true || false );
console.log( false || false );

console.log( "and: ");
console.log( true && true );
console.log( true && false );
console.log( false && false );


let x = 10;
let y = 20;

console.log( "a: ");
console.log( x > 1 || x < 20 );

console.log( "b: ");
console.log( x < 1 || x > 5 );

console.log( "c: ");
console.log( x < 1 || x > 15 );

console.log( "d: ");
console.log( x == 10 && y == 20 );

console.log( "e: ");
console.log( x == 10 && y != 15 );

console.log( "f: ");
console.log( x > 5 && x < 15 );
